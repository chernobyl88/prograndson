			<h4 class="upper margin-top-100 center col-lg-12 col-md-12 col-sm-12 col-xs-12">GRANDSON, année 1964</h4>
			<h3 class="upper margin-top-50 bold center col-lg-12 col-md-12 col-sm-12 col-xs-12">Pro Grandson Societe d'initiative</h3>
			<h4 class="upper margin-top-50 center col-lg-12 col-md-12 col-sm-12 col-xs-12">Statuts</h4>
			<div class="upper margin-bottom-20 bold col-lg-12 col-md-12 col-sm-12 col-xs-12">
				I. CONSTITUTION
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 1.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Pro Grandson, société d'initiative, a remplacé le 27 avril 1964 la Société de Développement de Grandson,
				dont elle a repris l'actif et le passif.
				La Société est régie par les présents statuts et par les articles 60 et suivants du Code Civil suisse
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 2.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Pro Grandson a pour but de s'intéresser et de participer à tout ce qui peut augmenter la prospérité et le développement de Grandson et des Tuileries, sur les plans économique, culturel et les loisirs. En collaboration avec l'Office Vaudois du Tourisme et avec l'Office pour le Développement du Nord Vaudois, Pro Grandson s'efforcera en particulier de promouvoir l'activité touristique régionale par la publicité, l'accueil et l'agrément des touristes, l'amélioration des équipements et toute autre mesure adéquate. Pro Grandson collaborera aussi avec toute autre société ou groupement poursuivant des buts analogues.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 3.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				La durée de la société est illimitée, son siège est à Grandson.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 4.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				L'année comptable commence le ler janvier et se termine le 31 décembre.
			</div>
			<div class="upper margin-bottom-20 bold col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				II. FONDS SOCIAL
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 5.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Il est constitué par :
				<br>
				la dotation "inaliénable" de la somme de Fr. 5.ooo. faite par le grand comité
				d'organisation du drame historique et populaire "Charles le Téméraire", joué à
				Grandson en 1897, et prélevé sur le bénéfice net de cette manifestation;
				les autres biens de la société.
			</div>
			<div class="upper margin-bottom-20 bold col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				III. RESSOURCES
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 6.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Les ressources de la société proviennent:
				<ul>
					<li>des cotisations annuelles</li>
					<li>des dons et legs</li>
					<li>des subventions</li>
					<li>du produit des taxes de séjour et de tourisme</li>
					<li>du revenu des biens de la société</li>
					<li>de toute autre ressource</li>
				</ul>
			</div>
			<div class="upper margin-bottom-20 bold col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				IV. MEMBRES
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 7.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Est membre de la société toute personne, physique, morale, ou collectivité, qui paye une cotisation annuelle dont le minimum est fixé par l'assemblée générale.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 8.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Responsabilité.<br>
				Les engagements de la société sont garantis uniquement par les biens de celle ci. Les sociétaires sont libérés de toute responsabilité individuelle.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 9.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Cotisations.<br>
				Tout sociétaire doit acquitter sa cotisation annuelle. En cas de non paiement et après avertissement, il cesse de faire partie de la société.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 10.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Démission.<br>
				Toute démission doit être annoncée par écrit au comité. La cotisation annuelle est due pour la totalité de l'exercice en cours.
			</div>
			<div class="upper margin-bottom-20 bold col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				V. ORGANES
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 11.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Les organes de la société sont:
				<ul>
					<li>l'assemblée générale</li>
					<li>le comité</li>
					<li>le bureau</li>
					<li>la commission de vérification des comptes.</li>
				</ul>
			</div>
			<div class="upper margin-bottom-20 bold col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				VI. ASSEMBLEE GÉNÉRALE
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 12.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Constitution.<br>
				L'assemblée générale est le pouvoir suprême de la société. Elle est régulièrement constituée quel que soit le nombre des membres présents.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 13.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				La société se réunit en assemblée générale ordinaire dans le premier semestre de chaque année. Il peut y avoir des assemblées extraordinaires si les circonstances l'exigent à la demande du comité ou de celle du cinquième au moins des sociétaires. La convocation a lieu par voie d'annonce dans le journal local, et par convocations individuelles adressées à chaque membre; les deux au plus tard cinq jours avant l'assemblée.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 14.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Attributions.<br>
				Les attributions de l'assemblée générale sont:
				<ul>
					<li>l'adoption et la modification des statuts</li>
					<li>la nomination des membres du comité et du président de la société</li>
					<li>la nomination des membres de la commission de vérification des comptes,</li>
					<li>l'examen et l'approbation des rapports du comité et de la commission de
					vérification des comptes à la fin de chaque exercice</li>
					<li>la fixation du minimum de la cotisation annuelle</li>
					<li>la discussion et la décision sur toute question qui n'est pas du ressort
					d'autres organes.</li>
				</ul>
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 15.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Vote et décision.<br>
				Chaque membre, quelle que soit sa cotisation possède une voix à l'assemblée générale. Les décisions sont prises à la majorité, à main levée. Le tiers des sociétaires présents peut requérir le scrutin secret.
			</div>
			<div class="upper margin-bottom-20 bold col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				VII. COMITE
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 16.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Constitution.<br>
				La société est administrée par un comité comprenant lo à 2o membres. En fait partie de droit un délégué de la Municipalité de Grandson.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 17.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Nomination.<br>
				Les membres du comité, sauf le délégué Municipal, sont nommés pour une période de trois ans et sont rééligibles.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 18.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Organisation.<br>
				Sauf pour la présidence, le comité s'organise. Le président de la société est en même temps président du bureau.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 19.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Les attributions du comité sont:
				<ul>
					<li>nomination du bureau </li>
					<li>désignation du président et des membres des commissions </li>
					<li>administration des affaires de la société </li>
					<li>représentation de la société </li>
					<li>toute action conforme au but de la société</li>
				</ul>
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 20.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Le comité est convoqué par le président, sur sa propre initiative ou à la demande d'au moins trois de ses membres. Il se réunit au minimum une fois par année.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 21.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Les décisions sont prises à la majorité absolue des membres présents; le président tranche en cas d'égalité.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 22.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				En cas de démission ou décès d'un de ses membres, le comité se complète lui même, sous réserve de faire ratifier son choix par la prochaine assemblée générale.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 23.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Le bureau composé de trois à sept membres, est chargé de l'expédition des affaires ne nécessitant pas la réunion du comité complet. Il se réunit aussi souvent que le président l'estime opportun.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 24.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Responsabilité.<br>
				Les membres du comité, lorsqu'ils agissent dans le cadre de leurs fonctions, n'assument pas de responsabilité personnelle du fait de la gestion des affaires de la société.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 25.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				La société est engagée vis à vis des tiers par la signature collective du président ou du vice président et d'un membre du bureau.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 26.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Les fonctions de membre du comité sont gratuites. Les débours sont remboursés.
			</div>
			<div class="upper margin-bottom-20 bold col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				VIII. COMMISSIONS DE VERFICATION DES COMPTES
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 27.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				La commission de vérification des comptes se compose de deux membres et d'un suppléant. Ils sont rééligibles, mais au maximum pendant deux ans.
			</div>
			<div class="upper margin-bottom-20 bold col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				IX. COMMISSIONS
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 28.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Le comité peut constituer toute commission jugée utile. L'organisation, le nombre de membres, les compétences seront adaptées aux circonstances.
			</div>
			<div class="upper margin-bottom-20 bold col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				X. REVISION DES STATUTS DISSOLUTION
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 29.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Toute modification ou révision des présents statuts ne pourra être décidée qu'à la majorité des deux tiers des membres présents à l'assemblée générale.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 30.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				La dissolution de la société ne pourra être décidée que lors d'une assemblée générale extraordinaire, et à la majorité des trois-quarts des membres présents.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Art. 31.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				En cas de dissolution, les biens de la société seront remis à la Municipalité de Grandson pour être gérés séparément ou affectés à des institutions locales d'utilité publique.
			</div>
			<div class="upper margin-bottom-20 bold col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				XI. DISPOSITIONS FINALES
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12 margin-top-20">
				Entrée en vigueur <br><br>
				Art. 32.
			</div>
			<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
				Les présents statuts abrogent ceux du 19 avril 1951 de la Société de Développement de Grandson. Ils ont été adoptés à l'assemblée générale du 2 novembre 1964.
